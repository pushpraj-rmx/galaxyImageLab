                            <h4>HOW IS THE MAMMOGRAPHY TEST PERFORMED?</h4>
                            <p>You will be asked to undress from the waist up and will be given a gown to wear. This test is performed by either sitting or standing.</p>

                            <p>One breast at a time is rested on a flat surface that contains the x-ray plate. A device called a compressor will be pressed firmly against the breast to help flatten out the breast tissue. The x-ray pictures are taken from several angles. You may be asked to hold your breath as each picture is taken.</p>

                            <p>Sometimes you will be asked to come back at a later date for more mammogram images. This does not always mean you have breast cancer. Rather, the doctor may simply need to recheck an area that could not be clearly seen on the first test.</p>

                            <p>Digital mammography is a newer technique that allows the x-ray image of the breast to be viewed and manipulated on a computer screen. It improves accuracy, but is not yet available everywhere.</p>

