                            <h2 class="sdetails-title">CT Scan :-</h2>
                            <h4>How is a CT Scan Performed?</h4>
                            <p>CT Scan is short-form for a Computerized Axial Tomography Scan. A CT Scan typically combines multiple rotating X-Rays along with hi-end computerized processing to produce a more detailed ‘picture’ of the inner structures of a body – including bones, tissues and organs. A CT Scan offers significantly greater detail and clarity as compared to a regular X-Ray (it’s a bit like looking inside a slice of bread, instead of an outer view of the entire loaf). A CT Scan is typically done on our spine, heart, head, chest, abdomen, abdomen and knee. During a CT Scan, the body is made to pass through a tunnel-like machine that rotates through a 360-degree arc as it takes X-Ray images in rapid succession. These images are subsequently fed into a computer to produce an ‘all-around’ 2D snapshot of any part of the body. A minimally invasive and painless procedure, a CT Scan can be done on any part of the body in little time. While essentially 2D in nature (i.e. producing two-dimensional image slices), a CT Scan can also be employed to create 3D images. A CT Scan cost depends on the body parts which you want to scan.</p>

                            <h4>CT Scan Procedure</h4>
                            <p>A CT Scan is usually done in our hospital or radiology clinic, and is performed by a qualified radiology technologist. The patient may be advised by the guiding medical professional or doctor to abstain from eating or drinking for a few hours before the procedure begins. When it is time for the Test, the patient will be required to lie on a table – usually on the back, though sometimes it may be sideways or face-down as well. The table then starts to make its way slowly inside a large and circular (donut-shaped) CT Machine that contains circling X-Ray beams within. As the body passes through the circle, the X-Ray beams capture a ring-like, cross-sectional image of the ‘slice’ of the body directly in its ‘gaze’ or range. After every ‘X-Ray shot’, the table (on which the patient is lying) will move a bit, enabling the machine to take the next ‘shot’. These shots are subsequently scanned and processed digitally to generate a series of 2D ‘slices’ of the body, each slice providing a detailed image of the bones, tissues, blood vessels and organs. The patient has to remain as still as possible during the scanning process to ensure best results. When the Scan is the operation, nobody is allowed to be present in the room – with the exception of the patient, of course. During the process, the patient can communicate with the radiologist professional who is conducting the Scan via intercom – and vice versa. A CT Scan can take anywhere between a few minutes to a half-hour at the max, and one can usually return home almost immediately once the procedure has been completed.</p>

                            <h4>How Long Does a CT Scan Take?</h4>
                            <p>A Simple CT Head (also known as NCCT Head or CT Brain) typically takes a few seconds inside a multi slice CT Scanner and does not require much pre-preparation. More complicated investigations such as CECT Abdomen (CT Abdomen with Contrast) or CT Triple Phase Abdomen may take hours of pre-preparation including oral and IV contrast administration.</p>

