                            <h2 class="sdetails-title">Fetal Echo :-</h2>
                            <h4>FETAL ECHO SCAN <20-28 weeks></h4>
                            <p>Routine ultrasound scans of your baby’s heart at 20 weeks may only pick up around 25% of defects depending on the skill of the doctor and number of views of the heart which are looked at.</p>

                            <p>Fetal Echo is a special test dedicated to looking at the heart structure and major vessels within and around the heart. A detailed scan like this can detect up to 75% of all heart defects. They can also provide you with much more information regarding a heart problem if it detected and treatment options that may be available.</p>

                            <p>The presence of a problem with baby’s heart can be an indicator of other structural or chromosomal abnormalities.</p>

                            <h4>Common problems which can show on the fetal echo scan are:</h4>
                            <ul>
                                <li>Major structural abnormalities of the heart where chambers or vessels are not connected properly;</li>
                                <li>Significant holes in the heart;</li>
                                <li>Significant abnormalities of the heart valves.</li>
                                
                            </ul>

                            <h4>Why is this test important?</h4>
                            <p>Abnormal results from fetal echocardiography can be inconclusive or require that you get more testing to find what’s wrong. Sometimes problems are ruled out and no further testing is needed. Once your doctor diagnoses a condition, you can better manage your pregnancy and prepare for delivery.</p>

                            <p>Results from this test will help you and your doctor plan treatments your baby may need after delivery, such as correctional surgery. You can also get support and counseling to help you make good decisions during the remainder of your pregnancy.</p>

