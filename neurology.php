                            <p>Neurology tests are conducted to diagnose peripheral neuropathy. Depending on patient’s symptoms, medical history and physical examination, various tests are recommended as diagnostic measure. Galaxy Imaging & Path Lab also conducts the following tests </p>

                            <ul>
                                <li><b>Electrodiagnostic tests-</b> This measure the electrical activity of muscles and nerves. By measuring the electrical activity, they are able to determine if there is nerve damage, the extent of the damage and potentially the cause of the damage. Frequently the neurologist will recommend common, non-invasive neurological evaluations such as electromyography (EMG) and nerve conduction velocity (NCV) testing. </li>
                                <li><b>Quantitative sensory testing (QST)-</b> It is method for diagnosing peripheral nervous system disorders, including chronic pain and pain related to various diseases, such as Diabetes and CRPS. QST essentially determines the sensation and pain thresholds for cold and warm temperatures, and the vibration sensation threshold by stimulating the skin and comparing the results to normative values built in the software. When the stimulus activates stimuli-specific receptors; the nerve fibres that innervate the receptors communicate the stimuli’s message to the central nervous system, where feeling occurs.</li>
                                <li><b>EEG-</b> An electroencephalogram (EEG) is a test that detects electrical activity in your brain using small, metal discs (electrodes) attached to your scalp. Your brain cells communicate via electrical impulses and are active all the time, even when you're asleep. This activity shows up as wavy lines on an EEG recording. 
An EEG is one of the main diagnostic tests for epilepsy. An EEG can also play a role in diagnosing
other brain disorders.</li>
                                
