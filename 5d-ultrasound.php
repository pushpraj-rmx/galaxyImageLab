                            <h4>Ultrasound</h4>
                            <p>Ultrasound is a high-frequency sound that you cannot hear but it can be emitted and detected by special machines. It travels freely through fluid and soft tissues and is a safe and painless scan. Our procedures at Galaxy Imaging & Path Lab conform to<b> world-class standards and PNDT guidelines. We are one of the few ultrasound centres in Delhi & first in Dwarka who have 5D Ultrasound Facility.</b></p>

                            <h4>Understanding the Ultrasound:</h4>
                            <p>An Ultrasound(sonography) is a Test procedure in which high-frequency sound waves (sonar and radio technology) are used to create ‘pictures’ of the inside of the body. Apart from being safe, an Ultrasound is also a painless and generally affordable process – making it popular with both patients and the medical fraternity. Ultrasound images are engineered in ‘real-time’, and let us see both the structure of our internal organs as well as the function (that is, the movement) of our blood vessels. While it has various kinds of medical uses and health applications, an Ultrasound is perhaps the most popular and preferred Test used to monitor the development of the foetus in pregnant women.</p>

                            <h4>What is the Procedure of an Ultrasound?</h4>

                            <p>An Ultrasound Test is carried out by a doctor or a Radiologist who has been specially trained for the purpose. An Ultrasound Test usually takes between 15 minutes to an hour, and normally doesn’t require any special preparation.</p>

                            <p>For an Ultrasound a bladder that is full of water is able to generate better pictures or images of the uterus and other organs (since air is a bad conductor of ultrasound and makes the scan a failure).</p>

                            <p>In the case of an External Ultrasound, the Transducer is placed over the part of the body (for example, the heart or the abdomen) that needs to be studied and has been administered a lubricating gel before-hand for this purpose. This process is usually free of any kind of pain or discomfort.</p>

                            <p>In the case of an Internal Ultrasound where the ovaries or urinary organs need to be studied in greater detail, the Transducer is inserted into the rectum (in the case of males) or the vagina (in the case of females). These studies can be routinely performed in approved outpatient clinics.</p>

                            <h4>Understanding the Science Behind Ultrasound Tests</h4>

                            <p>The term ‘Ultrasound’ literally means sounds belonging to a ‘frequency’ that humans are not able to ‘detect’ or hear. For most diagnostic applications, this frequency range lies between 2 and 18 megahertz (MHz). Higher frequencies can generate better Ultrasound image quality but are susceptible to getting absorbed (and therefore, ‘diminished in power’) by the skin and other soft tissues. Lower frequencies can penetrate deeper past the layers of skin & tissues but produce imagesmof a relatively lower resolution and quality.</p>

                            <p>During the process, the Radiologist will hold a wand-like device called the Transducer in his or her hand. The transducer is placed on the patient’s body externally. In some cases, insertion may be done via the rectum (for males) and vagina (for females) and the Transducer in such situations is ‘placed internally’.</p>

                            <p>Be it external or internal, sound waves are emitted by the machine which travels through the skin and tissue till it reaches a surface that is too dense for it to penetrate further. The sound waves are then reflected back (similar to the act of light reflecting off a mirror or, say, an echo of our own voice) – resulting in an image. This image or picture is also sometimes referred to as a Sonogram. Different shades of grey indicate different densities of the reflecting surface inside the body, which can be an organ or an internal structure. Despite being different technically, the terms Ultrasound and sonogram are sometimes used interchangeably.</p>


