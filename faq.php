                                            <h2>What is an ultrasound Scan?</h2>
											<p>Ultrasound is a procedure that uses high-frequency sound waves to view internal organs and produce images of the human body. Ultrasound is: Noninvasive, which means it does not penetrate the skin or body openings and Diagnostic which means it is used to determine what disease or condition is present The technical term for ultrasound imaging is sonography. The original ultrasound scanners produced still images, but modern scanners produce moving pictures, which are easier to interpret.</p>
											
