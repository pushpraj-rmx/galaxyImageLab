								 <h2>CYTOLOGY</h2>
								 <p>Cytopathology is study of cells and is commonly used to investigate diseases involving a wide range of body sites, often to aid in the diagnosis of cancer, but also in the diagnosis of some infectious diseases and other inflammatory conditions.</p> 
								 
								 
								 <h4 style="color:blue;">•	Interventional Cytology</h4>
								 <p><b>Interventional Cytology</b> i.e; <b>Fine-needle aspiration</b> (FNAC), performed by a pathologist or radiologist which involves usage of a needle attached to a syringe to collect cells from lesions or swellings in various body organs often with the application of negative pressure (suction) to increase yield.
                                FNAC can be performed under palpation guidance (i.e., the clinician can feel the lesion) in superficial regions like the neck, thyroid or breast ;
                                It may be also be assisted by ultrasound  for sampling of deep-seated non palpable lesions within the body.</P>
								
